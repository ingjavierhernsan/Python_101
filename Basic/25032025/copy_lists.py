# Copy a list

# Use the copy() method
thislist = ["apple", "banana", "cherry"]
mylist = thislist.copy()

print(mylist)

print("")

# Use the lis() method
thislist = ["apple", "banana", "cherry"]
mylist = list(thislist)

print(mylist)

print("")

# Use the slice operator
thislist = ["apple", "banana", "cherry"]
mylist = thislist[:]

print(mylist)

print("")

